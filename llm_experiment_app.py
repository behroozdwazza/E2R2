def shap_convention_warning(shap_scores: pd.DataFrame) -> str:
    if "shap_output_units" not in shap_scores.columns:
        return (
            "This SHAP file does not say which model output was explained. "
            "For the updated prompt, use SHAP files regenerated by the latest main E2R2 app."
        )
    units = shap_scores["shap_output_units"].dropna().astype(str).str.lower().unique().tolist()
    if units and not any("positive_class" in unit for unit in units):
        return (
            "This SHAP file may use the older predicted-class SHAP convention. "
            "Regenerate the case base and SHAP scores in the latest main E2R2 app before running final experiments."
        )
    return ""


