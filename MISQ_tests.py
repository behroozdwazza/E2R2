def read_uploaded_table(file_obj: io.BytesIO) -> pd.DataFrame:
    name = getattr(file_obj, "name", "").lower()
    file_obj.seek(0)
    if name.endswith(".csv"):
        return pd.read_csv(file_obj)
    if name.endswith(".xlsx") or name.endswith(".xls"):
        return pd.read_excel(file_obj)
    raise ValueError("Unsupported file type. Upload a CSV, XLSX, or XLS file.")


def esc(value: Any) -> str:
    return html.escape("" if value is None else str(value), quote=True)


def parse_post(handler: BaseHTTPRequestHandler) -> cgi.FieldStorage:
    return cgi.FieldStorage(
        fp=handler.rfile,
        headers=handler.headers,
        environ={
            "REQUEST_METHOD": "POST",
            "CONTENT_TYPE": handler.headers.get("Content-Type", ""),
        },
    )


def form_get(form: cgi.FieldStorage, key: str, default: Any = "") -> Any:
    if key not in form:
        return default
    value = form[key]
    if isinstance(value, list):
        return [item.value for item in value]
    return value.value


def load_upload(field: Any) -> Optional[pd.DataFrame]:
    if field is None or not getattr(field, "filename", ""):
        return None
    data = field.file.read()
    file_obj = io.BytesIO(data)
    file_obj.name = field.filename
    return read_uploaded_table(file_obj)


def shap_convention_warning(shap_scores: pd.DataFrame) -> str:
    if "shap_output_units" not in shap_scores.columns:
        return (
            "This SHAP file does not say which model output was explained. "
            "For the updated prompt, use SHAP files regenerated by the latest main E2R2 app."
        )
    units = shap_scores["shap_output_units"].dropna().astype(str).str.lower().unique().tolist()
    if units and not any("positive_class" in unit for unit in units):
        return (
            "This SHAP file may use the older predicted-class SHAP convention. "
            "Regenerate the case base and SHAP scores in the latest main E2R2 app before running final experiments."
        )
    return ""


