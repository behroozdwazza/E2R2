def normalize_label(value: Any) -> str:
    if pd.isna(value):
        return ""
    text = str(value).strip()
    if text.endswith(".0"):
        text = text[:-2]
    return text.lower()


def run_prediction_for_row(row_index: Any, variant: str, neighbors: int) -> Dict[str, Any]:
    api_key = STATE["api_key"]
    if not api_key:
        raise ValueError("Enter and save your OpenAI API key first.")
    holdout = STATE["holdout"]
    row = holdout.loc[holdout["row_index"] == row_index].iloc[0]
    retrieved = retrieve_cases(row_index, neighbors)
    prompt, ablation_notes = build_prompt(variant, row, retrieved, neighbors)
    raw = call_openai(api_key, STATE["model"], prompt)
    parsed = parse_llm_json(raw)
    actual = row.get(STATE["target_column"], "")
    predicted = parsed.get("final_predicted_outcome", parsed.get("predicted_outcome", ""))
    correct = normalize_label(actual) == normalize_label(predicted) if normalize_label(actual) else ""
    return {
        "row_index": row_index,
        "variant": variant,
        "variant_label": ABLATION_VARIANTS[variant]["label"],
        "actual_outcome": actual,
        "baseline_predicted_outcome": row.get("baseline_predicted_outcome", ""),
        "baseline_confidence": row.get("baseline_confidence", ""),
        "baseline_positive_probability": row.get("baseline_positive_probability", ""),
        "stage1_predicted_outcome": parsed.get("stage1_predicted_outcome", ""),
        "stage1_confidence_level": parsed.get("stage1_confidence_level", ""),
        "stage1_rationale": parsed.get("stage1_rationale", ""),
        "agreement": parsed.get("agreement", ""),
        "baseline_p_positive": parsed.get("baseline_p_positive", ""),
        "verification_rule_applied": parsed.get("verification_rule_applied", ""),
        "llm_predicted_outcome": predicted,
        "llm_confidence_level": parsed.get("final_confidence_level", parsed.get("confidence_level", "")),
        "llm_rationale": parsed.get("final_rationale", parsed.get("rationale", "")),
        "correct": correct,
        "ablation_notes": ablation_notes,
        "retrieval_mode": STATE["retrieval_mode"],
        "retrieved_case_ids": ", ".join(retrieved["case_id"].astype(str).tolist()),
        "prompt": prompt,
        "llm_raw_response": raw,
    }


def metrics_table(results: pd.DataFrame) -> pd.DataFrame:
    if results is None or results.empty or "correct" not in results.columns:
        return pd.DataFrame()
    rows = []
    for variant, group in results.groupby("variant", dropna=False):
        valid = group[group["correct"].isin([True, False])].copy()
        if valid.empty:
            continue
        actual = valid["actual_outcome"].map(normalize_label)
        pred = valid["llm_predicted_outcome"].map(normalize_label)
        labels = sorted(set(actual.dropna()) | set(pred.dropna()))
        positive = "1" if "1" in labels else labels[-1] if labels else ""
        tp = int(((actual == positive) & (pred == positive)).sum())
        fp = int(((actual != positive) & (pred == positive)).sum())
        tn = int(((actual != positive) & (pred != positive)).sum())
        fn = int(((actual == positive) & (pred != positive)).sum())
        accuracy = float((valid["correct"] == True).mean())
        precision = tp / (tp + fp) if tp + fp else 0.0
        recall = tp / (tp + fn) if tp + fn else 0.0
        f1 = 2 * precision * recall / (precision + recall) if precision + recall else 0.0
        rows.append(
            {
                "variant": variant,
                "variant_label": ABLATION_VARIANTS.get(variant, {}).get("label", variant),
                "n": len(valid),
                "accuracy": accuracy,
                "precision_positive": precision,
                "recall_positive": recall,
                "f1_positive": f1,
                "tp": tp,
                "fp": fp,
                "tn": tn,
                "fn": fn,
            }
        )
    return pd.DataFrame(rows)


def run_batch_job(row_indexes: List[Any], variants: List[str], neighbors: int) -> None:
    total = len(row_indexes) * len(variants)
    results = []
    set_batch_status(
        running=True,
        completed=0,
        total=total,
        current_row="",
        current_variant="",
        message="Starting MISQ ablation batch...",
        error="",
        finished=False,
        started_at=datetime.now().strftime("%H:%M:%S"),
    )
    try:
        completed = 0
        for row_index in row_indexes:
            for variant in variants:
                set_batch_status(
                    current_row=row_index,
                    current_variant=variant,
                    message=f"Processing row {row_index}, {ABLATION_VARIANTS[variant]['label']}...",
                )
                result = run_prediction_for_row(row_index, variant, neighbors)
                results.append(result)
                completed += 1
                result_df = pd.DataFrame(results)
                with STATE_LOCK:
                    STATE["batch_results"] = result_df
                    STATE["batch_metrics"] = metrics_table(result_df)
                set_batch_status(completed=completed, message=f"Completed {completed} of {total} predictions.")
        set_batch_status(
            running=False,
            completed=total,
            current_row="",
            current_variant="",
            message=f"Batch complete: {total} predictions generated.",
            error="",
            finished=True,
            finished_at=datetime.now().strftime("%H:%M:%S"),
        )
    except Exception as exc:
        set_batch_status(
            running=False,
            message="Batch stopped before completion.",
            error=str(exc),
            finished=True,
            finished_at=datetime.now().strftime("%H:%M:%S"),
        )


