class MISQTestsHandler(BaseHTTPRequestHandler):
    def do_GET(self) -> None:
        parsed = urlparse(self.path)
        if parsed.path == "/download":
            self.handle_download(parse_qs(parsed.query))
            return
        if parsed.path == "/batch-status":
            self.respond_json(get_batch_status())
            return
        self.respond(200, home())

    def do_POST(self) -> None:
        try:
            STATE["error"] = None
            parsed = urlparse(self.path)
            if parsed.path == "/upload":
                self.handle_upload()
            elif parsed.path == "/settings":
                self.handle_settings()
            elif parsed.path == "/predict-one":
                self.handle_predict_one()
            elif parsed.path == "/predict-batch":
                self.handle_predict_batch()
            else:
                self.respond(404, page("<section><h2>Not found</h2></section>"))
        except Exception as exc:
            STATE["error"] = f"{exc}\n\n{traceback.format_exc(limit=3)}"
            self.respond(500, home())

    def handle_upload(self) -> None:
        form = parse_post(self)
        case_base = load_upload(form["case_base"]) if "case_base" in form else None
        shap_scores = load_upload(form["shap_scores"]) if "shap_scores" in form else None
        holdout = load_upload(form["holdout"]) if "holdout" in form else None
        case_vectors = load_upload(form["case_vectors"]) if "case_vectors" in form else None
        holdout_vectors = load_upload(form["holdout_vectors"]) if "holdout_vectors" in form else None
        holdout_shap = load_upload(form["holdout_shap"]) if "holdout_shap" in form else None
        if case_base is None or shap_scores is None or holdout is None:
            raise ValueError("Upload case base, SHAP scores, and holdout dataset.")
        prepare_loaded_data(case_base, shap_scores, holdout, case_vectors, holdout_vectors, holdout_shap)
        self.redirect("/")

    def handle_settings(self) -> None:
        form = parse_post(self)
        api_key = form_get(form, "api_key", "")
        if api_key:
            STATE["api_key"] = api_key
        STATE["model"] = form_get(form, "model", STATE["model"])
        STATE["prediction_goal"] = form_get(form, "prediction_goal", "")
        STATE["include_baseline_signal"] = False
        variants = form_get(form, "variants", STATE["variants"])
        if isinstance(variants, str):
            variants = [variants]
        STATE["variants"] = [v for v in variants if v in ABLATION_VARIANTS] or ["full_e2r2"]
        self.redirect("/")

    def handle_predict_one(self) -> None:
        form = parse_post(self)
        row_index = form_get(form, "row_index")
        row_index = int(row_index) if str(row_index).isdigit() else row_index
        variant = form_get(form, "variant", "full_e2r2")
        neighbors = int(float(form_get(form, "neighbors", 5)))
        STATE["individual_result"] = run_prediction_for_row(row_index, variant, neighbors)
        self.redirect("/")

    def handle_predict_batch(self) -> None:
        form = parse_post(self)
        if get_batch_status().get("running"):
            raise ValueError("A batch is already running. Wait for it to finish before starting another.")
        if not STATE.get("api_key"):
            raise ValueError("Enter and save your OpenAI API key first.")
        offset = int(float(form_get(form, "offset", 0)))
        limit = int(float(form_get(form, "limit", 25)))
        neighbors = int(float(form_get(form, "neighbors", 5)))
        row_indexes = STATE["holdout"]["row_index"].iloc[offset : offset + limit].tolist()
        if not row_indexes:
            raise ValueError("No holdout rows were selected for this batch.")
        variants = [v for v in STATE.get("variants", []) if v in ABLATION_VARIANTS]
        if not variants:
            raise ValueError("Select at least one ablation variant.")
        with STATE_LOCK:
            STATE["batch_results"] = None
            STATE["batch_metrics"] = None
        set_batch_status(
            running=True,
            completed=0,
            total=len(row_indexes) * len(variants),
            message="Starting MISQ ablation batch...",
            error="",
            finished=False,
        )
        thread = threading.Thread(target=run_batch_job, args=(row_indexes, variants, neighbors), daemon=True)
        thread.start()
        self.redirect("/")

    def handle_download(self, query: Dict[str, Any]) -> None:
        name = query.get("name", [""])[0]
        if name == "batch":
            df = STATE.get("batch_results")
            filename = "misq_ablation_predictions.csv"
        elif name == "metrics":
            df = STATE.get("batch_metrics")
            filename = "misq_ablation_metrics.csv"
        else:
            self.respond(404, page("<section><h2>Download not found.</h2></section>"))
            return
        if df is None or df.empty:
            self.respond(404, page("<section><h2>No results to download.</h2></section>"))
            return
        payload = dataframe_to_csv_bytes(df)
        self.send_response(200)
        self.send_header("Content-Type", "text/csv; charset=utf-8")
        self.send_header("Content-Disposition", f'attachment; filename="{filename}"')
        self.send_header("Content-Length", str(len(payload)))
        self.end_headers()
        self.wfile.write(payload)

    def redirect(self, location: str) -> None:
        self.send_response(303)
        self.send_header("Location", location)
        self.end_headers()

    def respond(self, status: int, payload: bytes) -> None:
        self.send_response(status)
        self.send_header("Content-Type", "text/html; charset=utf-8")
        self.send_header("Content-Length", str(len(payload)))
        self.end_headers()
        self.wfile.write(payload)

    def respond_json(self, payload: Dict[str, Any]) -> None:
        body = json.dumps(payload, default=str).encode("utf-8")
        self.send_response(200)
        self.send_header("Content-Type", "application/json; charset=utf-8")
        self.send_header("Content-Length", str(len(body)))
        self.end_headers()
        self.wfile.write(body)

    def log_message(self, format: str, *args: Any) -> None:
        return


def run() -> None:
    server = None
    port = DEFAULT_PORT
    for candidate in range(DEFAULT_PORT, DEFAULT_PORT + 30):
        try:
            server = ThreadingHTTPServer((HOST, candidate), MISQTestsHandler)
            port = candidate
            break
        except OSError:
            continue
    if server is None:
        raise RuntimeError("No open local port was found for the MISQ tests app.")
    url = f"http://{HOST}:{port}"
    print(f"MISQ E2R2 Ablation Tests running at {url}")
    threading.Timer(0.8, lambda: webbrowser.open(url)).start()
    server.serve_forever()


if __name__ == "__main__":
    run()
