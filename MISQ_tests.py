def call_openai(api_key: str, model: str, prompt: str) -> str:
    payload: Dict[str, Any] = {
        "model": model,
        "input": prompt,
        "max_output_tokens": response_token_limit(model),
    }
    if not model.startswith("gpt-5") and not model.startswith("o"):
        payload["temperature"] = 0.2
    request = Request(
        "https://api.openai.com/v1/responses",
        headers={"Authorization": f"Bearer {api_key}", "Content-Type": "application/json"},
        data=json.dumps(payload).encode("utf-8"),
        method="POST",
    )
    try:
        with urlopen(request, timeout=150) as response:
            data = json.loads(response.read().decode("utf-8"))
    except HTTPError as exc:
        detail = exc.read().decode("utf-8", errors="replace")
        try:
            detail = json.loads(detail).get("error", {}).get("message", detail)
        except Exception:
            pass
        raise RuntimeError(f"OpenAI API request failed ({exc.code}): {detail}") from exc
    if data.get("output_text"):
        return data["output_text"]
    chunks = []
    for item in data.get("output", []):
        for content in item.get("content", []):
            if content.get("type") in {"output_text", "text"}:
                chunks.append(content.get("text", ""))
    return "\n".join(chunks).strip()


def parse_llm_json(text: str) -> Dict[str, Any]:
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        match = re.search(r"\{.*\}", text, re.DOTALL)
        if not match:
            return {}
        try:
            return json.loads(match.group(0))
        except json.JSONDecodeError:
            return {}


def retrieve_cases(row_index: Any, k: int) -> pd.DataFrame:
    case_base = STATE["case_base"]
    holdout = STATE["holdout"]
    holdout_vectors = STATE["holdout_vectors"]
    case_vectors = STATE["case_vectors"]
    row_to_position = {value: pos for pos, value in enumerate(holdout["row_index"].tolist())}
    if row_index not in row_to_position:
        raise ValueError(f"Holdout row_index {row_index} was not found.")
    vector = holdout_vectors[row_to_position[row_index]]
    rows = case_base.copy()
    rows["similarity"] = case_vectors @ vector
    rows = rows[rows["row_index"] != row_index]
    return rows.sort_values("similarity", ascending=False).head(k).reset_index(drop=True)


def feature_definition(feature: str) -> str:
    shap_scores = STATE["shap_scores"]
    if "definition" not in shap_scores.columns:
        return ""
    matches = shap_scores.loc[shap_scores["feature"].astype(str) == str(feature), "definition"].dropna()
    return "" if matches.empty else str(matches.iloc[0])


def feature_lines(row: pd.Series) -> str:
    target_column = STATE["target_column"]
    excluded = set(BASELINE_COLUMNS) | {"row_index", target_column}
    lines = []
    for feature, value in row.items():
        if feature in excluded:
            continue
        definition = feature_definition(feature)
        suffix = f" ({definition})" if definition else ""
        lines.append(f"- {feature}{suffix}: {value}")
    return "\n".join(lines)


def peer_blocks(retrieved: pd.DataFrame, include_shap: bool) -> str:
    shap_scores = STATE["shap_scores"]
    blocks = []
    for _, peer in retrieved.iterrows():
        header = (
            f"Case {peer.case_id}: outcome={peer.predicted_outcome}, "
            f"retrieval similarity={float(peer.similarity):.3f}"
        )
        if not include_shap:
            blocks.append(header)
            continue
        peer_shap = shap_scores[shap_scores["case_id"] == peer["case_id"]].sort_values("rank").head(5)
        units = (
            peer_shap["shap_output_units"].dropna().iloc[0]
            if "shap_output_units" in peer_shap.columns and not peer_shap["shap_output_units"].dropna().empty
            else "probability"
        )
        shap_lines = [
            f"  - {item.feature}: value={item.value}, SHAP contribution to positive-class probability={item.shap_score:.4f} ({units})"
            for item in peer_shap.itertuples()
        ]
        blocks.append("\n".join([header, *shap_lines]))
    return "\n".join(blocks)


def neighbor_distribution(retrieved: pd.DataFrame) -> str:
    counts = retrieved["predicted_outcome"].astype(str).value_counts()
    total = int(counts.sum())
    if total == 0:
        return "- No retrieved neighbors."
    lines = []
    for outcome, count in counts.items():
        percent = (count / total) * 100
        lines.append(f"- {outcome}: {count} of {total} retrieved neighbors ({percent:.1f}%)")
    return "\n".join(lines)


def shap_alignment(row: pd.Series, retrieved: pd.DataFrame) -> str:
    shap_scores = STATE["shap_scores"]
    relevant = shap_scores[shap_scores["case_id"].isin(retrieved["case_id"])].copy()
    rows = []
    for feature, group in relevant.groupby("feature"):
        mean_shap = group["shap_score"].mean()
        mean_abs = group["abs_shap_score"].mean()
        positive_count = int((group["shap_score"] > 0).sum())
        negative_count = int((group["shap_score"] < 0).sum())
        peer_values = group["value"].dropna().astype(str).value_counts().head(3)
        holdout_value = row.get(feature, "")
        matching_count = int((group["value"].astype(str) == str(holdout_value)).sum())
        if mean_shap > 0:
            direction = "increases positive-class probability"
        elif mean_shap < 0:
            direction = "decreases positive-class probability"
        else:
            direction = "has near-neutral direction"
        rows.append(
            {
                "mean_abs": mean_abs,
                "line": (
                    f"- {feature}: holdout={holdout_value}; peer values="
                    f"{', '.join([f'{value} ({count})' for value, count in peer_values.items()])}; "
                    f"mean SHAP={mean_shap:.4f}, mean |SHAP|={mean_abs:.4f}; "
                    f"{direction}; sign counts +/{positive_count}, -/{negative_count}; exact matches={matching_count}"
                ),
            }
        )
    rows = sorted(rows, key=lambda item: item["mean_abs"], reverse=True)[:5]
    return "\n".join(item["line"] for item in rows) or "- No SHAP alignment rows were available."


def recurring_top_predictors(row: pd.Series, retrieved: pd.DataFrame, per_case_top_k: int = 5, top_n: int = 5) -> str:
    shap_scores = STATE["shap_scores"]
    relevant = shap_scores[shap_scores["case_id"].isin(retrieved["case_id"])].copy()
    if "rank" in relevant.columns:
        relevant = relevant[relevant["rank"] <= per_case_top_k].copy()
    if relevant.empty:
        return "- No recurring top-predictor rows were available."
    total_cases = max(int(retrieved["case_id"].nunique()), 1)
    rows = []
    for feature, group in relevant.groupby("feature"):
        holdout_value = row.get(feature, "")
        peer_values = group["value"].dropna().astype(str).value_counts().head(3)
        mean_abs = float(group["abs_shap_score"].mean())
        mean_shap = float(group["shap_score"].mean())
        case_count = int(group["case_id"].nunique())
        if mean_shap > 0:
            direction = "usually increases positive-class probability"
        elif mean_shap < 0:
            direction = "usually decreases positive-class probability"
        else:
            direction = "has mixed overall direction"
        definition = feature_definition(feature)
        suffix = f" ({definition})" if definition else ""
        rows.append(
            {
                "case_count": case_count,
                "mean_abs": mean_abs,
                "line": (
                    f"- {feature}{suffix}: appears in {case_count}/{total_cases} retrieved cases' top {per_case_top_k} predictors; "
                    f"holdout={holdout_value}; precedent values={', '.join([f'{value} ({count})' for value, count in peer_values.items()])}; "
                    f"mean |SHAP|={mean_abs:.4f}; {direction}"
                ),
            }
        )
    rows = sorted(rows, key=lambda item: (item["case_count"], item["mean_abs"]), reverse=True)[:top_n]
    return "\n".join(item["line"] for item in rows)


def attribution_block(row_index: Any, retrieved: pd.DataFrame) -> Tuple[str, str]:
    holdout_shap = STATE.get("holdout_shap")
    if holdout_shap is not None and not holdout_shap.empty:
        key_col = "row_index" if "row_index" in holdout_shap.columns else "case_id" if "case_id" in holdout_shap.columns else ""
        if key_col:
            matches = holdout_shap[holdout_shap[key_col].astype(str) == str(row_index)].copy()
            if not matches.empty:
                matches = matches.sort_values("abs_shap_score", ascending=False).head(12)
                lines = [
                    f"- {item.feature}: value={getattr(item, 'value', '')}, SHAP contribution to positive-class probability={item.shap_score:.4f}, |SHAP|={item.abs_shap_score:.4f}"
                    for item in matches.itertuples()
                ]
                return "holdout_shap", "\n".join(lines)
    shap_scores = STATE["shap_scores"]
    relevant = shap_scores[shap_scores["case_id"].isin(retrieved["case_id"])].copy()
    if relevant.empty:
        return "none", "- No attribution rows were available."
    agg = (
        relevant.groupby("feature", as_index=False)
        .agg(mean_shap=("shap_score", "mean"), mean_abs_shap=("abs_shap_score", "mean"))
        .sort_values("mean_abs_shap", ascending=False)
        .head(12)
    )
    lines = [
        (
            f"- {row.feature}: mean SHAP across retrieved cases={row.mean_shap:.4f}, "
            f"mean |SHAP|={row.mean_abs_shap:.4f}; "
            f"{'increases' if row.mean_shap > 0 else 'decreases' if row.mean_shap < 0 else 'has neutral effect on'} "
            "positive-class probability"
        )
        for row in agg.itertuples()
    ]
    return "retrieved_precedent_shap_proxy", "\n".join(lines)


def labels_for_prompt() -> str:
    holdout = STATE["holdout"]
    target = STATE["target_column"]
    if target and target in holdout.columns:
        labels = sorted(holdout[target].dropna().astype(str).unique().tolist())
        if labels:
            return ", ".join(labels)
