def progress_script() -> str:
    return """
    <script>
      const panel = document.getElementById("batch-progress");
      async function refreshBatchStatus() {
        if (!panel) return;
        try {
          const response = await fetch("/batch-status", { cache: "no-store" });
          if (!response.ok) return;
          const status = await response.json();
          document.getElementById("batch-progress-fill").style.width = `${Math.max(0, Math.min(100, Number(status.percent || 0)))}%`;
          document.getElementById("batch-progress-label").textContent = status.message || "Waiting for batch status...";
          document.getElementById("batch-progress-count").textContent = `${status.completed || 0} of ${status.total || 0}`;
          document.getElementById("batch-progress-current").textContent =
            status.current_row ? `Current row: ${status.current_row}; variant: ${status.current_variant || ""}` : "";
          document.getElementById("batch-progress-error").textContent = status.error ? `Error: ${status.error}` : "";
          if (status.running) {
            window.setTimeout(refreshBatchStatus, 1200);
          } else if (status.finished && !window.batchCompletionReloaded) {
            window.batchCompletionReloaded = true;
            window.setTimeout(() => window.location.reload(), 1000);
          }
        } catch (_) {
          window.setTimeout(refreshBatchStatus, 2000);
        }
      }
      if (panel && panel.dataset.active === "true") refreshBatchStatus();
    </script>
    """


